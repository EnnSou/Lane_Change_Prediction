function parsave(fname, prepedGSR, prepedECG, prepedRSP, ...
                        prepedGSRraw, prepedECGraw, ...
                        prepedRSPraw, prepedOBD)
    save(fname, 'prepedGSR', 'prepedECG', 'prepedRSP', 'prepedGSRraw', ...
                'prepedECGraw', 'prepedRSPraw', 'prepedOBD');
end