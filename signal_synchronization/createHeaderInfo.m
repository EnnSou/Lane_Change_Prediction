function inputData = createHeaderInfo(inputData)

    % TextIndex is a structure store the name of all signals, first column
    % is TimeStamp
    inputData.Text_Index(1, 1)    = {'Time'};
    inputData.Text_Index(2:3, 1)	= inputData.Ecg_Txt(1, 2:3)';
    
    % Save Rsp data information into TextIndex
    inputData.Text_Index(size(inputData.Text_Index, 1) + 1 : ...
                         size(inputData.Text_Index, 1) + ...
                length(inputData.Rsp_Txt(1, 2:end)), 1) = ...
                inputData.Rsp_Txt(1, 2:end)';
            
    % Save Gsr data information into TextIndex
    inputData.Text_Index(size(inputData.Text_Index, 1) + 1 : ...
                         size(inputData.Text_Index, 1) + ...
                length(inputData.Gsr_Txt(1, 2:end)), 1) = ...
                inputData.Gsr_Txt(1, 2:end)';
    
    % Save GSR RAW data information into TextIndex
    inputData.Text_Index(size(inputData.Text_Index, 1) + 1 : ...
                         size(inputData.Text_Index, 1) + ...
                length(inputData.GSR_RAW_Txt(1, 3)), 1) = ...
                inputData.GSR_RAW_Txt(1, 3);
    
    % Final column store the target Information
    inputData.Text_Index(size(inputData.Text_Index, 1) + 1, 1) = ...
                    cellstr(inputData.target_Txt{1, 16});
                
end